# Python Program to Demonstrate All Python Data Types.
a = 10
print("Integer :", a, type(a))

b = 10.5
print("Float :", b, type(b))

c = "Hello, Python"
print("String :", c, type(c))

d = True
print("Boolean :", d, type(d))

e = [1, 2, 3, "Hello World", 63.5]
print("List :", e, type(e))

f = (1, 2, 3, "Python Practical File", 41.54)
print("Tuple :", f, type(f))

g = {1, 2, 3, "By Tanmay CSE Section 3", 4.5}
print("Set :", g, type(g))

h = {"Name" : "Tanmay", "Enrollment no" : 16616403224}
print("Dictionary :", h, type(h))

i = None
print("NoneType :", i, type(i))

